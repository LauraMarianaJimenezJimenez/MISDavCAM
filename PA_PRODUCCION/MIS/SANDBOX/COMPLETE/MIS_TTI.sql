    CAST(AVG(a.RATE) * IF(a.IND_VOLATILITY = 'Y', a.PERC_VOLATILITY, 1) AS decimal(30, 10)) AS TTI, a.IND_VOLATILITY, a.PERC_VOLATILITY 
FROM (
