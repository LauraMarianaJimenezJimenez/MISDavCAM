        WHEN ISNULL(PARAM_ST.METHOD_TTI,PARAM_ST2.METHOD_TTI) = 'ST'
        THEN DATEDIFF(TO_TIMESTAMP(SALIDA_DWH.FINISH_DATE,'yyyyMMdd'), TO_TIMESTAMP(SALIDA_DWH.START_DATE,'yyyyMMdd'))
        ELSE NULL
