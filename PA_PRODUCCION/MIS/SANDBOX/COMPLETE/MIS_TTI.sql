        (TO_TIMESTAMP(b.DATA_DATE, 'yyyyMMdd') BETWEEN SUBDATE(TO_TIMESTAMP('${var:periodo}', 'yyyyMMdd'), a.AVG_PERIOD - 1) AND TO_TIMESTAMP('${var:periodo}', 'yyyyMMdd'))
