PARTITION (DATA_DATE='${var:periodo}') 
SELECT a.COD_CONT, a.IDF_CTO, a.COD_GL, a.DES_GL, a.COD_ACCO_CENT, a.COD_OFFI, 
    a.COD_BLCE_STATUS, CONCAT(a.COD_VALUE, '_', c.CUENTA) AS COD_VALUE, a.COD_CURRENCY, a.COD_ENTITY, a.COD_PRODUCT, a.COD_SUBPRODUCT, 
