    GROUP by NO_OPERACION, Op_PRODUCTO, IND_CONTABLE, CATEGORIA) b
ON REGEXP_REPLACE(a.IDF_CTO, '^0*', '') = REGEXP_REPLACE(b.NO_OPERACION, '^0*', '') 
AND STRRIGHT(a.COD_PRODUCT, 3) = b.CATEGORIA
LEFT JOIN MIS_TMP_APR_PROVISIONS_STG_3 c  
ON a.COD_VALUE = c.TIPO AND a.COD_CURRENCY = c.MONEDA 
AND IF(c.OP_PRODUCTO = 'N/A', 'N/A', b.OP_PRODUCTO) = c.OP_PRODUCTO 
AND IF(c.IND_CONTABLE = 'N/A', 'N/A', b.IND_CONTABLE) = c.IND_CONTABLE 
AND IF(c.CATEGORIA = 'N/A', 'N/A', b.CATEGORIA) = c.CATEGORIA
WHERE a.PL IS NOT NULL
;

